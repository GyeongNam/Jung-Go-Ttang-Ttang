              ③
              회원은 본조에 의한 회사의 조치에 대하여 항변의 사유가 있는 경우 이에 대하여 항변을 할 수 있습니다.

              제 45 조 (신용점수)
              판매회원의 신용점수는 평가되며, 판매회원이 구매서비스를 이용하는 경우 구매서비스이용에 따른 신용점수의 평가와 중복적으로 적용됩니다.

