              서비스 저작권 및 지적재산권은 회사에 귀속됩니다. 단, 제휴계약에 따라 제공된 저작물 등 은 제외합니다.

              ②
              회사가 제공하는 서비스의 디자인, 회사가 만든 텍스트, 스크립트(script), 그래픽, 회원 상호간 전송 기능 등 회사가 제공하는 서비스에 관련된 모든 상표, 서비스 마크, 로고 등에 관한 저작권 기타 지적재산권은 대한민국 및 외국의 법령에 기하여 회사가 보유하고 있거나 회사에게 소유권 또는 사용권이 있습니다.

              ③
              회원은 본 이용약관으로 인하여 서비스를 소유하거나 서비스에 관한 저작권을 보유하게 되는 것이 아니라, 회사로부터 서비스의 이용을 허락받게 되는 바, 서비스는 정보 취득 또는 개인적인 용도로만 제공되는 형태로 회원이 이용할 수 있습니다.

              ④
              회원은 명시적으로 허락된 내용을 제외하고는 서비스를 통해 얻어지는 회원 상태정보를 영리 목적으로 사용, 복사, 유통하는 것을 포함하여 회사가 만든 텍스트, 스크립트, 그래픽의 회원 상호간 전송기능 등을 복사하거나 유통할 수 없습니다.

              ⑤
              회사는 서비스와 관련하여 회원에게 회사가 정한 이용조건에 따라 계정, 아이디, 컨텐츠 등을 이용할 수 있는 권한만을 부여하며, 회원은 이를 양도, 판매, 담보제공 등의 처분행위를 할 수 없습니다.

              ⑥
              회사는 서비스를 통해 얻어지는 회원 게시물(판매회원, 구매회원 정보 및 기타 거래 관련 정보)의 정보를 회사의 영리를 목적으로 회원의 허락 없이 사용할 수 있습니다.

              ⑦
              회사가 서비스 내에 게시한 게시물 저작권은 특별한 사정이 없는 한 회사에 귀속됩니다.

              ⑧
              회원이 생성한 컨텐츠에 대해서는 회사와 해당 회원에게 공동 소유권 또는 사용권이 있습니다. 이러한 컨텐츠 등은 검색결과, 정보성 이메일, 회사가 운영하는 다른 서비스, 프로모션 등에 노출될 수 있으며, 회사는 해당 목적을 위하여 필요한 범위 내에서는 일부 수정, 복제, 편집되어 게시될 수 있습니다.

              ⑨
              회원은 언제든지 고객센터 또는 서비스 내 관리 기능을 통해 해당 게시물에 대하여 삭제, 검색결과 제외, 비공개 등의 조치를 취할 수 있습니다.

              제 4 장 구매 서비스 및 구매 부가 서비스 이용
              제 26 조 (매매계약의 체결 및 대금 결제)
              ①
              상품의 매매계약은 판매회원이 제시한 상품의 판매 조건에 응하여 청약의 의사표시를 하고 이에 대하여 판매회원이 승낙의 의사표시를 함으로써 체결됩니다.

              ②
              이용자, 결제대행업체, 금융기관 등과의 사이에 발생한 분쟁은 당사자들 간의 해결을 원칙으로 하며, 회사는 이와 관련해서 어떠한 책임도 지지 않고 해당 대행업체 또는 기관의 약관이 우선됩니다.

              ③
              회사는 회원이 현금, 카드 기타의 방법으로 매매 대금을 결제할 수 있는 방법을 제공합니다.

              ④
              매매 대금의 결제와 관련하여 구매회원이 입력한 정보 및 그 정보와 관련하여 발생한 책임과 불이익은 전적으로 구매회원이 부담하여야 합니다.

              ⑤
              상품을 주문한 후 일정 기간 내에 매매대금을 결제하지 않을 경우 회사는 당해 주문을 회원의 동의 없이 취소할 수 있습니다.

              ⑥
              회사는 구매회원의 상품 매매계약 체결 내용을 나의 구매내역을 통해 확인할 수 있도록 조치하며, 매매계약의 취소 방법 및 절차를 안내합니다.

              ⑦
              회사는 구매회원이 매매대금 결제 시 사용한 결제수단에 대해 정당한 사용권한을 가지고 있는지 여부를 확인할 수 있으며, 이에 대한 확인이 완료될 때까지 거래진행을 중지하거나, 확인이 불가한 해당거래를 취소할 수 있습니다.

              ⑧
              구매회원이 실제로 결제하는 금액은 판매회원이 정한 공급원가, 기본이용료, 상품에 적용된 배송비, 옵션상품의 상세내역 등이 적용된 금액(실구매액)이며 구매회원에게 발행되는 구매증빙서(현금영수증, 세금계산서, 신용카드매출전표 등)는 실구매액으로 발행됩니다.

              제 27 조 (배송)
              ①
              배송 소요기간은 입금 또는 대금결제 확인일의 익일을 기산일로 하여 배송이 완료되기까지의 기간을 말합니다. 공휴일과 기타 휴무일 또는 천재지변 등 불가항력의 사유가 발생하면 그 해당 기간은 배송 소요기간에서 제외됩니다.

              ②
              회사는 배송과 관련하여 판매회원과 구매회원, 배송업체, 금융기관 등과의 사이에 발생한 분쟁은 당사자들 간의 해결을 원칙으로 하며, 회사는 어떠한 책임도 부담하지 않고 해당 배송업체의 약관이 우선됩니다.

              제 28 조 (취소)
              ①
              회원은 구매한 상품이 발송되기 전까지 구매를 취소할 수 있으며, 배송중인 경우에는 취소가 아닌 반품절차에 따라 처리됩니다.

              ②
              결제 완료 후 배송대기 상태에서 취소 접수시 특별한 사정이 없는 한 즉시 취소가 됩니다.

              제 29 조 (반품)
              ①
              회원은 배송완료된 날부터 관계법령에 따라 48시간 이내에 반품 신청할 수 있습니다.

              ②
              반품에 관한 일반적인 사항은 전자상거래 등에서의 소비자보호 관한 법률(이하 ‘전자상거래법’이라 함) 등 관련법령이 판매회원이 제시한 조건보다 우선합니다.

              ③
              반품 신청시 반품 송장번호를 미기재하거나 반품사유에 관하여 판매회원에게 정확히 통보(또는 서면)하지 않을 시 반품처리 및 환불이 지연될 수 있습니다.

              제 30 조 (환불)
              ①
              카드결제를 통한 구매건의 환불은 원칙적으로 카드결제 취소를 통해서만 가능합니다.

              ②
              회원은 현금잔고로 환불된 금액을 통장으로 입금 받고자 하는 경우에는 별도로 지정한 자신의 출금계좌로 현금출금요청을 통해 출금할 수 있습니다.

              제 31 조 (반품/교환/환불의 적용 배제)
              구매회원은 다음 아래의 경우에는 환불 또는 교환을 요청할 수 없습니다.

              ①
              구매회원의 귀책사유로 말미암아 상품이 멸실, 훼손된 경우

              ②
              구매회원의 사용 또는 일부 소비에 의하여 상품의 가치가 현저히 감소한 경우

              ③
              시간의 경과에 의하여 재판매가 곤란할 정도로 상품의 가치가 현저히 감소한 경우

              ④
              복제가 가능한 상품의 포장을 훼손한 경우

              ⑤
              기타 구매회원이 환불 또는 교환을 요청할 수 없는 합리적인 사유가 있는 경우

              제 32 조 (구매안전 서비스)
              ①
              회사는 회사에서 이루어지는 선불식 통신판매에 있어서 구매회원이 지급하는 결제대금을 예치하고 배송이 완료된 후 재화 또는 용역의 대금을 판매회원에게 지급함으로써 구매회원의 안전을 도모합니다.

              ②
              회사는 구매회원에게 상품을 공급받은 사실을 재화 등을 공급받은 날로부터 3영업일 이내에 통보하여 주도록 요청합니다.

              ③
              회사는 구매회원이 재화 등을 공급받은 날부터 3영업일이 지나도록 재화 등을 공급받은 사실을 통보하지 아니한 때에는 구매회원이 재화 등을 공급받은 것으로 간주하여 판매회원에게 결제대금을 지급할 수 있습니다.

              제 5 장 중고땅땅 판매서비스의 이용
              제 33 조 (전자상거래 시스템(플랫폼) 제공 서비스)
              회사는 전자상거래 시스템(플랫폼) 제공을 통해 판매회원이 다양한 형태로 상품을 판매할 수 있도록 지원합니다.

              제 34 조 (판매회원의 판매활동)
              ①
              상품 및 용역의 판매는 판매회원 등록이 완료됨과 동시에 가능하며, 이를 위해서 판매회원은 상품 및 용역에 관한 정보를 직접 등록, 관리하여야 합니다. 이때 상품 및 용역의 판매가격은 표준마진율, 배송비, 서비스 이용료 등을 고려하여 판매회원이 스스로 결정합니다.

              ②
              판매회원은 재고 수량 등 수시로 변동되는 사항에 대한 데이터를 적절히 관리하여야 하며, 데이터를 허위로 기재할 수 없습니다.

              ③
              판매회원이 (주)중고땅땅 회사를 통하지 않고 제3의 외부업체의 프로그램을 이용하여 상품을 등록하는 경우 그 과정에서 유발되는 각종 기술적, 법적 문제에 대해 회사는 아무런 책임을 지지 아니하며 이로 인해 발생되는 모든 손해에 대해 판매회원이 전적으로 부담합니다.

              ④
              판매회원은 전자상거래 등에서의 소비자보호에 관한법률(이하 "전소법"이라 함) 등 상품 및 용역의 판매와 관련하여 법령이 요구하는 사항을 준수하여야 합니다.

              ⑤
              판매회원은 회사의 서면에 의한 사전 승인 없이 (주)중고땅땅 회사의 상호나 로고 등을 사용할 수 없습니다.

              ⑥
              판매회원은 (주)중고땅땅 회사를 통하지 않고 구매회원에게 직접 상품을 판매하거나 이를 유도하여서는 아니되며, 이를 위반할 경우 회사는 당해 판매회원을 탈퇴시킬 수 있습니다.

              ⑦
              판매회원은 판매된 상품 및 용역에 대한 보증 서비스를 자신의 책임과 비용으로 실시하여야 합니다.

              ⑧
              판매회원은 자신의 판매회원 정보란에 회사가 정하는 절차에 따라 인증 받은 유선전화 또는 휴대폰 번호를 대표번호로서 설정하고 항상 최신 정보로서 유지하여야 합니다. 판매회원이 본 항의 의무를 이행하지 않을 경우 회사는 이행 완료 시까지 해당 판매회원의 물품 등록과 편집을 제한 할 수 있습니다.

              제 35 조 (서비스 이용료)
              ①
              회사는 필요한 경우 서비스이용료를 신설, 변경할 수 있으며, 신설 또는 변경사항은 회사가 제공하는 서비스화면을 통하여 공지합니다.

              ②
              서비스이용료는 포인트 사용 등으로 결제할 수 있으며, 회사와 판매회원간의 협의 또는 회사의 내부 사정에 따라 요율, 결제방법 등이 변경될 수 있습니다.

              제 36 조 (상품의 배송)
              ①
              구매회원의 주문에 따른 결제가 완료되면 회사는 판매회원이 주문 정보를 확인할 수 있도록 조치를 취하고 판매회원은 당해 주문 정보에 따라 배송을 하여야 합니다.

              ②
              판매회원은 주문내역 확인 후 배송 중 상품 파손되지 않도록 적절한 포장을 한 후 배송의 증명 또는 조회가 가능한 물류대행(택배)업체에 결제완료 기준 3영업일 내에 접수해야 합니다.

              ③
              판매회원은 송장번호 직접 등록이 필요한 경우, (주)중고땅땅 회사에게 송장번호 등의 발송 관련 데이터를 입력하여 발송이 완료되었음을 증명하여야 합니다.

              ④
              판매회원이 기한 내에 발송하지 아니하거나, 구매회원의 귀책사유가 아닌 배송지 오류 등으로 인해 구매회원이 상품을 정상적으로 수령하지 못한 경우 판매회원은 그에 관한 모든 책임을 부담하여야 합니다.

              ⑤
              회사는 제3자와 업무제휴를 통해 통합택배, 커머스 서비스 등을 실시할 수 있습니다.

              제 37 조 (판매회원의 배송 의무)
              ①
              주문이 접수되면 판매회원은 발주서 발급일 포함 3영업일 이내에 (주)중고땅땅 회사를 통하여 발송예정일 및 발송과 관련된 데이터를 입력하여야 합니다.

              ②
              판매회원은 상품을 발송하기 전에 반드시 주문이 취소되었는지 최종 확인해야 하며 상품발송 후 배송 정보 관련 미입력 건에 대해서 구매회원이 회사로 취소요청을 하는 경우 판매회원에게 통보 없이 취소될 수 있으며 상품회수에 소요되는 비용은 판매회원의 부담이 됩니다.

              제 38 조 (판매회원 배송정보 입력 의무 및 분쟁 처리 의무)
              ①
              회원은 상품을 물류대행업체에 배송 위탁한 후, 즉시 상품 발송과 관련된 데이터를 입력하여 발송이 완료되었음을 증명하여야 합니다.

              ②
              회사는 정산금 지급의 목적 등으로 경우에 따라 판매회원에게 배송완료의 증빙을 요청할 수 있으며, 판매회원은 회사 요청이 있은 날로부터 7일 이내에 해당 자료를 제출하여야 합니다.

              ③
              판매회원이 전항의 증빙을 제출하지 않거나, 허위 배송정보를 입력함으로써 발생하는 회사의 손해 및 제반 문제에 대한 일체의 책임은 판매회원에게 있으며, 회사는 판매회원에게 신용점수 차감 등으로 불이익을 줄 수 있습니다.

              ④
              상품 배송시 판매회원은 배송 방식을 자체배송으로 선택하여 발송할 수 있으나 배송의 증명 또는 배송 추적이 되지 않아 클레임 발생할 경우, 상품발송 관련 증빙을 제시하여야 합니다.

              ⑤
              판매회원은 상품별 배송비 수취 여부를 설정할 수 있으며, 구매회원의 결제 방법 선택에 따라 해당 금액을 확인하여 배송절차를 완료할 의무를 부담합니다.

              ⑥
              판매회원이 발송정보를 정확하게 입력하였음에도 불구하고 구매회원으로부터 상품 배송과정에서 하자가 발생한 것으로 분쟁이 제기되는 경우 판매회원은 자신의 책임하에 하자발생의 원인을 규명하고 분쟁을 해결하여야 합니다.

              제 39 조 (판매회원의 취소 및 반품)
              ①
              판매회원은 반품상품 수령일로부터 72시간 이내에 환불 또는 환불에 필요한 조치를 취하여야하며 구매회원의 반품 요청 후 72시간내에 판매회원이 아무런 조치를 취하지 않은 체결 건은 자동 거래완료 될 수 있습니다.

              ②
              판매회원은 상품하자나 오배송 등을 인정한 상태에서 구매회원에게 반품 또는 교환 배송비를 부담시켜서는 안되며 반품배송비를 선결제한 구매회원이 반품상품 발송시 추가 부담한 착불배송료는 구매회원의 요청에 따라 반환하여야 합니다.

              ③
              구매회원은 상품 배송완료일로부터 48시간 이내에 반품요청을 할 수 있으며 판매회원은 구매회원이 이 기간 내 반품이나 교환을 요청하는 경우 요청에 따라 반품 또는 교환을 해 주어야 합니다. 단, 구매회원의 귀책사유로 상품 훼손된 경우, 사용이나 일부 소비로 인해 상품의 가치가 현저히 감소한 경우, 복제 가능한 상품의 포장을 훼손한 경우, 기타 법령에 의하여 반품이 제한되어 있는 경우는 예외로 합니다.

              ④
              판매회원은 판매내역, 푸시를 통한 반품신청을 수시로 확인하여 조치를 취하여야 합니다. 회사를 통한 반품신청을 확인하지 못한 것은 판매회원의 과실로 인정하고 판매회원에게 사전협의를 하지 않은 것을 사유로 반품을 거부하여서는 아니 됩니다.

              ⑤
              판매회원이 구매회원의 청약철회를 제한하고자 하는 경우, 상품페이지 등 구매회원이 쉽게 알 수 있는 곳에 반품제한 사유를 게재하여야 하나 부당한 근거로 반품을 거부하는 경우에는 전자상거래법에 규정된 반품규정이 판매회원이 지정한 반품조건보다 우선합니다.

              ⑥
              구매회원이 물류나 가구직배송 등 자체 배송한 상품에 대하여 반품을 원할 경우, 판매회원은 구매회원에게 반품절차를 정확히 안내한 후 반품을 진행하여야 합니다.

              ⑦
              판매회원은 주문건에 대한 취소 및 반품 처리시 정확한 귀책 사유를 입력하여야 하며, 이와 관련된 클레임 발생 시 판매회원에게 패널티가 부과될 수 있습니다.

              제 40 조 (교환 및 환불)
              ①
              구매회원이 상품 수령 후 교환이나 환불을 요청하는 경우 판매회원은 전자상거래법 등 관련 법률에 따라 반품을 받은 후 교환이나 환불을 해주어야 하며, 추가로 발생하는 비용은 교환이나 환불의 책임이 있는 측에서 부담합니다.

              ②
              판매회원은 상품의 하자 또는 사용상의 안전성에 결함이 있는 경우 전량 리콜(수리, 교환, 환불)하여야 하며, 리콜에 따른 모든 비용을 부담하여야 합니다.

              ③
              판매회원은 청약철회가 불가능한 상품의 경우 해당 내용을 상품 상세페이지에 명시하고, 배송 시에도 포장 또는 기타 소비자가 쉽게 알 수 있는 곳에 명기하여야 합니다.

              제 41 조 (판매대금의 정산)
              ①
              판매회원은 기본이용료(표준마진율), 배송비 및 서비스 이용료 등을 고려하여 상품의 판매 대금을 자유롭게 정할 수 있습니다.

              ②
              회사의 판매회원에 대한 정산 일자 등 구매안전서비스(결제대금예치)의 운영에 관한 구체적인 내용은 회사가 제공하는 서비스화면을 통하여 공지합니다.

              제 42 조 (정산의 보류)
              ①
              회사는 판매회원의 귀책사유로 인해 발생한 비용을 판매대금 정산 시 정산금액에서 공제한 후 나머지 금액에 한하여 판매회원에게 지급할 수 있습니다.

              ②
              회사는 장기간 배송지연건을 배송완료 건으로 간주하여 주문 절차를 종결할 수 있고, 판매대금의 정산은 향후 구매회원의 환불 요청에 대비하여 일정기간 유보할 수 있습니다.

              ③
              판매회원의 채권자의 신청에 의한 판매대금의 가압류, 압류 및 추심명령 등 법원의 결정이 있을 경우 회사는 판매회원과 채권자 간의 합의 또는 채무액의 변제 등으로 동 결정이 해제될 때까지 판매대금의 정산을 중지하거나 제3채무자로서 판매회원의 정당한 채무를 변제할 수 있습니다.

              ④
              회사는 아래의 사유가 발생하는 경우 상품판매대금의 정산을 유보할 수 있습니다.

              1.
              법원 등 제3자가 자격을 갖추어 상품판매대금 지급보류를 요청한 경우 회사는 보류요청이 해제될 때까지 관련 상품판매대금 송금을 보류하거나 정당한 채권자에게 지급할 수 있습니다.

              2.
              판매회원과 구매회원 간에 동일한 유형의 클레임이 지속적으로 발생하는 경우, 구매회원의 클레임에 대비하여 일정기간 판매대금의 정산을 유보할 수 있습니다.

              3.
              판매회원은 폐업신고 이후에는 원칙적으로 판매가 금지되며, 회사는 판매회원의 폐업 이후에 판매된 상품의 판매대금에 관해서는 정산을 하지 않는 것을 원칙으로 합니다.

              4.
              본 조에서 정한 사항 외에도 법률의 규정에 의하거나 합리적인 사유가 있는 경우에는 회사는 판매회원에게 통지하고 판매대금의 전부 또는 일부에 대한 정산을 일정 기간 유보할 수 있습니다.

              제 43 조 (납세관리에 관한 정책의 수립)
              회사는 부가가치세법 제33조 2항의 규정에 근거하여 납세관리에 관한 정책을 수립하여 운영할 수 있습니다.

