              회사는 매매부적합상물품이 발견된 경우 사전 통보없이 당해 상품의 광고를 삭제하거나 그 판매를 중지시킬 수 있으며, 당해 상품이 기판매된 경우 그 거래를 취소할 수 있습니다.

              ③
              회사는 매매부적합물품을 등록한 판매회원의 회원 자격을 정지시키거나 탈퇴시킬 수 있으며, 매매부적합물품으로 인하여 입은 손해를 당해 판매회원에게 청구할 수 있습니다.

              제 47 조 (취득한 구매회원 정보의 보호)
              ①
              판매회원은 판매서비스의 이용에 따라 취득한 구매회원 등 타인의 개인정보를 이 약관에서 정한 목적 이외의 용도로 사용할 수 없으며, 이를 위반할 경우 당해 판매회원은 관련 법령에 의한 모든 민사, 형사상의 법적 책임을 지고 자신의 노력과 비용으로 회사를 면책시켜야만 하며 회사는 당해 판매회원을 탈퇴시킬 수 있습니다.

              ②
              회사는 개인정보 보호를 위하여 배송 등의 목적으로 판매회원에게 공개되어 있는 구매회원의 개인정보를 상당 기간이 경과한 후 비공개 조치할 수 있습니다.

              ③
              전기통신사업법 등 관련 법령이 규정하는 적법한 절차에 따라 수사관서 등이 회사에 판매회원에 관한 정보의 제공을 요청한 경우, 회사는 그에 관한 자료를 제출할 수 있습니다.

              제 48 조 (금지행위)
              ①
              판매회원은 아래에 해당하는 행위를 하여서는 아니되며 이를 위반한 경우 회사는 개별적으로 규정하고 있는 제재조치를 취할 수 있습니다.

              1.
              허위체결 금지

              -
              허위체결이란 판매회원이 상품 노출순위 및 판매회원 신용등급 조작, 구매평 조작 등 매출증대를 위해 본인 또는 타인의 ID를 사용하여 판매회원 본인의 상품을 구매하는 체결 행위를 말하며, 회사는 이를 금지하고 있습니다.

              -
              허위체결 적발 시 회사는 회원 ID에 대하여 신용점수 차감, 이용제한, 계약해지, 정산금 지급보류(2개월 이내) 등 필요한 조치를 취할 수 있습니다.

              -
              허위체결 적발 시 회사는 사안에 따라 판매회원에게 부가적인 확인 요청 및 자진취소또는 반품을 요구할 수 있으며, 판매회원은 이에 협조할 의무가 있습니다.

              2.
              특정 서비스 가입조건 판매 금지

              -
              특정 서비스 가입조건 판매행위란 등록상품, 서비스의 판매 및 광고 이외의 목적으로 상품을 등록하여 판매하는 행위를 말하며, 회사는 이를 금지하고 있습니다.   (ex. 인터넷 서비스 가입 조건으로 지급되는 사은품을 등록하여 판매하는 행위)

              -
              해당 거래 적발 시 회사는 해당 제품의 삭제, 신용점수의 불이익 및 판매중지 등의 조치를 취할 수 있습니다.

              3.
              카테고리 위반등록 금지 외

              -
              관계없는 카테고리에 상품을 등록하는 것으로서, 회사는 이를 금지하고 있습니다.

              -
              기타 비정상적인 방법으로 상품을 노출하는 모든 행위를 금지합니다.

              -
              카테고리 위반등록 적발 시 회사는 적발 횟수에 따라 신용점수 차감, 상품 판매 제한, 판매회원 ID중지의 조치를 취할 수 있습니다.

              4.
              부정키워드 사용금지

              -
              상품 등록 시 상품리스트 노출을 목적으로 상품명에 아래와 같은 유형의 부정 키워드를 사용하는 것을 금지하고 있습니다.

              가.
              판매회원의 스토어명, 유사 스토어명을 기재하는 경우

              나.
              상품명에 해당상품과 무관한 검색키워드를 기재하는 경우

              -
              부정키워드 적발 시 회사는 신용점수 차감, 정산기일 조정 등 제재를 취할 수 있습니다.

              5.
              내 가게 등 html tag 사용금지

              -
              회사는 html tag 사용이 허용된 메뉴 이외에 판매회원이 임의로 html tag를 사용하는 것을 금지하고 있습니다.

              -
              임의 html tag 사용 적발시 상품 삭제, 신용점수 차감, 이용정지 및 회원탈퇴 등의 조치를 취할 수 있습니다.

              6.
              상표권 침해 금지

              -
              판매회원은 타인의 상표나 로고를 사용할 때 반드시 사전에 해당 권리자에게 서면으로 사용허락을 받아야 합니다. 다음의 예와 같이 타인의 등록상표를 허가 받지 않고 무단사용하는 것은 부정경쟁방지 및 영업비밀보호에 관한 법률상의 부정경쟁 행위에 해당되며(부정경쟁방지 및 영업비밀보호에 관한 법률 제2조), 상표권 침해의 소지가 있으므로 사용을 금합니다.

              가.
              ~스타일, ~style, ~st, ~스탈, ~형, ~ 풍, ~타입 등의 문구를 사용하는 경우

              나.
              유명 상품명의 일부를 삭제하여 사용하는 경우

              -
              회사는 타인(개인 또는 회사)이 소유한 상표를 무단으로 도용한 이미테이션(모조)상품의 판매를 제재하고 있습니다.

              가.
              상품의 상품명 또는 상세설명에 A급, 특A급, SA급, 이미테이션 등의 문구를 기재한 경우

              7.
              저작권 침해 금지

              -
              상품 등록 시 타인이 촬영한 사진, 타인이 창작한 이미지나 문구 등 인간의 사상 또는 감정 을 표현한 모든 창작물을 무단으로 사용하는 경우 저작권 침해에 해당하므로 회사는 이를 금지하고 있습니다. 단, 구매회원이 중고땅땅에서 구매한 상품을 되팔기 하는 경우 판매 회원으로부터 관련 이미지 및 텍스트 내용에 대한 사용 허락을 받은 경우는 예외입니다.

              -
              아래와 같은 경우에 저작권 침해에 해당합니다.

              가.
              타인이 창작, 제작한 사진이나 이미지, 제조사 등의 카탈로그 또는 문구 등을 동의없이사용하거나 링크한 경우

              나.
              허가 없이 신문 기사 게재하거나, 기사 일부분을 편집하여 상품 판매 홍보에 사용

              다.
              방송화면을 캡쳐하여 판매 상품의 홍보에 사용하는 경우

              라.
              타 사이트 상품의 상품평을 도용하여 상품명, 상세설명 등에 사용하는 경우

              8.
              초상권 및 성명권 침해 금지

              -
              연예인의 사진 및 동영상은 연예인 본인 및 소속 매니지먼트 회사, 해당 광고회사 등에 대한 저작권 또는 초상권 침해에 해당하여 이를 금하고 있습니다.

              가.
              잡지 등에 수록된 연예인 사진을 스캔하여 사용하는 경우

              나.
              연예인 사진 동영상을 무단으로 사용한 경우

              다.
              연예인 이름+스타일과 같은 문구를 제목, 키워드, 상세설명에 무단으로 사용하는 경우

              라.
              방송화면을 캡쳐하여 사용한 경우

              -
              초상권 및 성명권 침해 적발 시 해당 상품은 판매 중지됩니다.

              -
              타인의 초상권 및 성명권을 침해했을 경우 해당 상품은 상품등록이 거절되거나 제한상품으로 등록되고, 신용점수 차감, 상품삭제, 판매회원ID 중지 등의 패널티가 적용됩니다.

              9.
              기타 권리침해 행위 금지

              -
              회사는 특허권 등 산업재산권을 침해하는 행위 및 명예 등 타인의 권리를 침해하는 행위를 금지하고 있습니다.

              -
              특허권 등 산업재산권을 침해하는 행위 및 명예훼손 등 타인의 권리를 침해했을 경우 해당 상품은 상품등록이 거절되거나 제한상픔으로 등록되고, 신용점수 차감, 상품삭제, 판매자ID 중지 등의 패널티가 적용됩니다.

              10.
              기타 법령 준수 의무 위반한 상품의 판매 금지행위

              -
              전기용품, 의료기기, 식품, 화장품 등 개별 법령에 의하여 판매에 일정한 자격이 필요한 경우나 상품 자체에 유통을 위한 검증이 필요한 경우에는 관련 법령이 요구하는 조건을 갖추지 아니한 상품의 판매 또는 관련 법령이 요구하는 조건을 갖추지 아니한 판매회원의 상품 판매는 금지됩니다.

              -
              법령상 요구되는 준수의무를 다하지 않은 경우에는 경고의 횟수에 따라 패널티가 부여될 수 있습니다. 다만, 사전 공지 또는 통지가 있은 일부 상품의 경우 아래 경고 횟수와 무관 하게 회원 자격이 제한될 수 있습니다.

              11.
              연락두절

              연락두절이란 판매회원이 회원가입시 기재한 유선 또는 이메일, 기타의 방법을 통하여 구매회원 및 회사가 연락을 시도하였음에도 불구하고 일체의 응대가 진행되지 않는 상태를 말합니다. 판매회원은 휴업, 영업정지, 폐업 등 정상적인 판매활동이 불가한 상태 또는 이와 같은 상태가 예상되는 경우 이를 즉시 회사에 통보하여야 하며 정상적인 판매활동이 불가한 상태 또는 이와 같은 상태라 하더라도 전자상거래 등에서의 소비자보호에 관한 법률 등 관련법에 따른 취소, 반품, 교환 등의 신속한 처리를 위하여 최선의 노력을 다하여야 합니다.

              제 7장 포인트
              제49조 (포인트 획득)
              ①
              “포인트”라 함은 “중고땅땅”에서 제공하는 서비스 구매 시 대금 결제 수단으로 사용할 수 있는 결제수단을 의미합니다.

              ②
              포인트의 부여 및 사용에 관한 상세한 사항은 회사가 정한 정책에 따르며, 회사는 포인트 서비스 페이지에 관련 링크 등을 통하여 이를 이용회원에게 안내합니다.

              ③
              회원은 ‘중고땅땅’에서 제공하는 미션을 수행하는 등 일정한 행위를 완료함으로써 “포인트”를 획득할 수 있습니다.

              ④
              “포인트”의 획득 방식과 금액은 “회사”의 정책에 따라 달라질 수 있습니다. “회사”는 “포인트” 관련 정책을 수립·시행·변경하는 경우 적용일자 7일 전부터 적용일자 전일까지 공지사항을 통해 공지합니다. 다만, 이용회원에게 불리한 약관의 개정의 경우에는 적용일자 30일 이전에 공지합니다.

              ⑤
              미션은 “회사”와 스폰서 제휴사의 감독하에 진행됩니다. 회사의 결정은 “포인트” 획득과 관련된 모든 문제에 대해 최종적이며 구속력을 가집니다. 획득자가 부적격하다고 판단되는 경우, “회사”는 “포인트”를 제공하지 않을 수 있습니다.

              ⑥
              획득한 “포인트”는 “서비스”내 “회원정보”화면에서 확인할 수 있습니다.

              ⑦
              서버에 대한 제3자의 모든 불법적인 접속 또는 서버 불법적인 이용으로부터 발생하는 오류로 인해 “회사”의 서버에 입력된 포인트와 “이용자의 내 정보 상의 포인트” 사이에 차이가 나는 경우 서버의 수치를 기준으로 하며, 서버와 어플리케이션의 비동기화로 인한 적립금 차이는 정정이 불가능합니다.

              ⑧
              “포인트”는 양도, 대여, 담보의 목적으로 될 수 없습니다. 다만 “회사”가 인정하는 경우 예외로 합니다.

              제50조 (포인트의 정정, 취소 및 소멸)
              ①
              “포인트” 획득이 확정된 후 오류가 발생한 경우 “회원”은 오류 확인일로부터 30일 이내에 “회사”에 정정 요구를 할 수 있으며, “회사”는 정당한 요구임이 확인된 경우 정정 요구일로부터 90일 이내에 정정하여야 합니다.

              ②
              “클라이언트” 변경, 해킹, 매크로 등 부정한 방법으로 “포인트”를 획득한 경우 획득된 “포인트”는 소멸되며, “회원” 자격이 상실됩니다.

              ③
              “포인트”는 획득일로부터 365일간 유효하며, 365일 이내 사용하지 않을 경우 적립된 순서대로 순차 소멸됩니다.

              제 51조 포인트 적립 및 사용조건의 안내
              중고땅땅는 포인트 적립/사용 조건 및 관련 제약사항을 중고땅땅 모바일앱/웹등을 통하여 회원에게 고지하며, 포인트 적립조건이 바뀌더라도 본 약관이 변경되지는 않습니다.

              제 52조 회원탈퇴와 포인트 처리
              ①
              회원이 탈퇴 요청을 하는 경우, 회원은 탈퇴 요청일 현재까지 회원이 사용하지 않은 포인트에 대한 권리도 포기하여야 하며, 그 권리를 포기하지 않는 경우에는 탈퇴가 확정되지 아니합니다.

              ②
