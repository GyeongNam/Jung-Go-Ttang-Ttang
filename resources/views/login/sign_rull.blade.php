              9.
              통신 비밀보호법 위반물(영상+음성 가능한 CCTV, 핀홀 카메라 등)

              10.
              보증서나 감정서가 없는 보석류

              11.
              전문적인 달러 환전으로 인한 상행위

              12.
              음란한 사진, 언어, 영상, 신호를 사용하여 성적인 수치심을 자극하는 음란표현물

